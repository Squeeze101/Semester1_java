public class loopfor {
        public static void main(String[] args) {
        
        System.out.println("Perulangan (loop) naik");
        for(int i=1; i<=5; i++)
            System.out.println("Indeks Naik ke ="+i);
        System.out.println ("Perulangan (loop) turun");
        for(int i=5; i>0;--i)
            System.out.println("Indeks Naik ke ="+i);    
       }  
}
